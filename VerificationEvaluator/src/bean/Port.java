package bean;

public class Port{
	int port_begin = -1;
	int port_end = -1;
	public int getPort_begin() {
		return port_begin;
	}
	public void setPort_begin(int port_begin) {
		this.port_begin = port_begin;
	}
	public int getPort_end() {
		return port_end;
	}
	public void setPort_end(int port_end) {
		this.port_end = port_end;
	}
}